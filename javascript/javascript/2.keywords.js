var a; //declaration
a=10; //initilization
console.log(a)//10

a='hello'//re-intilization
console.log(a);//hello  // all cases are possible 

var a;//re-declaration
a=true;
console.log(a);//true


// let keyword
let b; //declaration
b=10; //initilization
console.log(b)//10

b='hello'//re-intilization
console.log(b);//hello

//   let a;//re-declaration is not possible with let keyword


